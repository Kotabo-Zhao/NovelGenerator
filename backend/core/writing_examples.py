# 按场景关键词检索示例
SCENE_KEYWORDS = {
    "开头": ["fantasy_opening"],
    "战斗": ["fantasy_fight"], "打架": ["fantasy_fight"], "比武": ["fantasy_fight"],
    "感情": ["fantasy_emotion"], "告别": ["fantasy_emotion"], "回忆": ["fantasy_emotion"],
    "日常": ["urban_daily"], "生活": ["urban_daily"], "上班": ["urban_daily"],
    "对话": ["dialogue_subtext", "dialogue_conflict"], "吵架": ["dialogue_conflict"],
    "悬疑": ["mystery_tension"], "案件": ["mystery_tension"],
    "短句": ["rhythm_short"],
}


def get_examples_for_scene(scene_desc: str, count: int = 2) -> str:
    """根据场景描述自动匹配并返回相关示例"""
    import random
    matched_keys = set()
    for keyword, keys in SCENE_KEYWORDS.items():
        if keyword in scene_desc:
            matched_keys.update(keys)
    
    if not matched_keys:
        # 默认返回通用示例
        matched_keys = {"rhythm_short", "dialogue_subtext"}
    
    examples = []
    for key in random.sample(list(matched_keys), min(len(matched_keys), 3)):
        pool = EXAMPLES.get(key, [])
        if pool:
            examples.append(random.choice(pool))
    
    if not examples:
        examples = [random.choice(EXAMPLES["rhythm_short"])]
    
    return "\n\n---\n\n".join(examples[:count])


def inject_examples_into_prompt(prompt: str, scene_desc: str = "") -> str:
    """在 prompt 中注入写作示例"""
    examples = get_examples_for_scene(scene_desc)
    return f"""{prompt}

---
【写作参考 — 以下是真人写作示例，请模仿其节奏、句长变化、细节选择和留白技巧，但不要抄内容】

{examples}
---
"""
